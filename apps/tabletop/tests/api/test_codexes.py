class CodexViewSetTestMixin(object):
    fixtures = [
        'load_universes.json',
        'load_fractions.json',
        'load_sides.json',
        'load_armies.json',
        'load_rank_types.json',
        'load_ranks.json',
        'load_users.json',
        'load_sides.json',
        'load_codexes.json',
    ]

    def setUp(self):
        self.maxDiff = None
        # update content types
        self.side = Side.objects.get(pk=1)
        self.side_ct = get_content_type(Side)
        self.user = User.objects.get(username='user')
        self.admin = User.objects.get(username='admin')
        self.other_user = User.objects.get(username='user2')

        Codex.objects.filter(content_type_id=17).update(
            content_type=self.side_ct)

        self.codex = Codex.objects.get(pk=1)
        self.url_detail = reverse(
            'api:codex-detail', args=(self.codex.pk, ))

        self.url_list = reverse('api:codex-list')
        self.url_post = self.url_list
        self.url_put = self.url_detail
        self.url_patch = self.url_detail
        self.url_delete = self.url_detail

        self.put = {
            'content_type': self.side_ct.pk,
            'object_id': self.side.pk,
            'revisions': '1,2,3,4',
            'title': 'Vanilla codex put'
        }
        self.patch = {
            'title': 'No vanilla codex'
        }
        self.post = {
            'content_type': self.side_ct.pk,
            'object_id': self.side.pk,
            'revisions': '1,2,3',
            'title': 'Vanilla codex'
        }
        self.object_detail_response = {
            'content_type': self.side_ct.pk,
            'object_id': 1,
            'plain_side': 'Space Marines',
            'revisions': '1,2,3,4,5',
            'title': 'Space Marines',
            'url': 'http://testserver/api/codexes/1/'
        }


class CodexViewSetAnonymousUserTest(CodexViewSetTestMixin, TestHelperMixin,
                                    APITestCase):
    def setUp(self):
        super(CodexViewSetAnonymousUserTest, self).setUp()

    # test anonymous user
    def test_get_detail(self):
        response = self.client.get(self.url_detail, format='json')
        self.assertEqual(response.status_code, status.HTTP_200_OK)
        self.assertEqual(response['Content-Type'], 'application/json')
        load = json.loads(response.content)
